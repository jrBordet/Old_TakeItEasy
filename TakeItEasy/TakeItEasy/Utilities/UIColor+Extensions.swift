import UIKit

extension UIColor {
    static var primayBlack: UIColor {
        return UIColor(named: "primayBlack") ?? .clear
    }
    
    static var secondaryBlack: UIColor {
        return UIColor(named: "secondaryBlack") ?? .clear
    }
}
